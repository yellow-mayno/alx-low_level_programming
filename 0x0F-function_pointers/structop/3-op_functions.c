/**
* op_add - add
* @a: 1
* @b: 3
* Return: +
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - sub
* @a: 1
* @b: 3
* Return: 0
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - mul
* @a: 1
* @b: 2
* Return: 0
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - div
* @a: 1
* @b: 2
* Return: 0
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - mod
* @a: 1
* @b: 2
* Return: sdfsd
*/
int op_mod(int a, int b)
{
	return (a % b);
}
